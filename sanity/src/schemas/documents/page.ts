import {defineField, defineType} from 'sanity'
import pageBuilder from '../../utils/pageBuilder'

export default defineType({
  name: 'page',
  title: 'Page',
  type: 'document',
  fields: [
    defineField({
      name: 'title',
      title: 'Title',
      type: 'string',
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      validation: (Rule) => Rule.required(),
      options: {
        source: 'title',
        maxLength: 96,
      },
    }),
    pageBuilder,
  ],
  preview: {
    select: {
      title: 'title',
    },
  },
})
